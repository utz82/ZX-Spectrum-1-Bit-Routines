		$t[0] = $len2*256+$code;
		$t[6] = 0;
		print sprintf(" fdb \$%x,\$%x,\$%x,\$%x,\$%x,\$%x,\$%x\n", @t);
		}
		next;
	}
	
	s/db /fcb /;
	s/dw /fdb /;
	
	s/^\s*;/*/;
	
	print $_,"\n";
